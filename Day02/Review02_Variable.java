/*
 * 1Byte = 8bit
 * Integer saved in Computer: 4Bytes
 * Creating Variable: Declaration & Assignment
 */

public class Review02_Variable {

	public static void main(String[] args) {

		// Example of Declaration
		int x;
		// Example of Assignment
		x = 10;
		// Example of Declaration+Assignment
		int y = 10;
		// Can Declare Multiple Variables
		int sum, diff;
		// Can use Variables to Assign 
		sum = x+y;
		diff = x-y;
		
		System.out.println(sum);
		System.out.println(diff);
	}

}
